// Precompute a 32x32x32 RGB lookup table for fast nearest-color lookup
function buildColorLUT(palette) {
  const size = 32;
  const lut = new Uint8Array(size * size * size * 3);
  const colors = palette;
  const n = colors.length;

  for (let ri = 0; ri < size; ri++) {
    const r = (ri * 255 / (size - 1)) | 0;
    for (let gi = 0; gi < size; gi++) {
      const g = (gi * 255 / (size - 1)) | 0;
      for (let bi = 0; bi < size; bi++) {
        const b = (bi * 255 / (size - 1)) | 0;
        let bestDist = Infinity;
        let bestR = 0, bestG = 0, bestB = 0;
        for (let i = 0; i < n; i++) {
          const cr = colors[i][0], cg = colors[i][1], cb = colors[i][2];
          const dr = r - cr, dg = g - cg, db = b - cb;
          const dist = dr * dr + dg * dg + db * db;
          if (dist < bestDist) {
            bestDist = dist;
            bestR = cr; bestG = cg; bestB = cb;
          }
        }
        const idx = (ri * size * size + gi * size + bi) * 3;
        lut[idx] = bestR;
        lut[idx + 1] = bestG;
        lut[idx + 2] = bestB;
      }
    }
  }
  return lut;
}

// Look up nearest palette color via LUT
function lookupLUT(lut, r, g, b) {
  const size = 32;
  const ri = (r * (size - 1) / 255 + 0.5) | 0;
  const gi = (g * (size - 1) / 255 + 0.5) | 0;
  const bi = (b * (size - 1) / 255 + 0.5) | 0;
  const idx = (ri * size * size + gi * size + bi) * 3;
  return [lut[idx], lut[idx + 1], lut[idx + 2]];
}
